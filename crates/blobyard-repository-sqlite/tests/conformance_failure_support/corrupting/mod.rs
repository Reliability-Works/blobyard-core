pub(crate) struct Corrupting<'a, T> {
    inner: &'a T,
    corruption: Corruption,
    inbox_list_calls: AtomicUsize,
}

impl<'a, T> Corrupting<'a, T> {
    pub(crate) const fn new(inner: &'a T, corruption: Corruption) -> Self {
        Self {
            inner,
            corruption,
            inbox_list_calls: AtomicUsize::new(0),
        }
    }
}
