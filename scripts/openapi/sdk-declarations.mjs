import { schemaType } from "./schema-tools.mjs";

function quoted(value) {
  return JSON.stringify(value);
}

function inputType(item) {
  const fields = [];
  if (item.contract.bodyName !== undefined) {
    fields.push(`readonly body: Schemas[${quoted(item.contract.bodyName)}];`);
  }
  if (item.contract.queryInput !== undefined) {
    fields.push(
      `readonly query${item.contract.queryInput.required ? "" : "?"}: ${schemaType(item.contract.queryInput.schema)};`,
    );
  }
  if (item.contract.pathInput !== undefined) {
    fields.push(`readonly path: ${schemaType(item.contract.pathInput.schema)};`);
  }
  if (item.contract.idempotency) {
    fields.push(`readonly idempotencyKey${item.contract.idempotencyRequired ? "" : "?"}: string;`);
  }
  fields.push("readonly signal?: AbortSignal;");
  return `Readonly<{ ${fields.join(" ")} }>`;
}

function schemaMap(document) {
  return Object.entries(document.components.schemas)
    .map(([name, schema]) => `  readonly ${quoted(name)}: ${schemaType(schema)};`)
    .join("\n");
}

export function sdkDeclarations(document, items) {
  const metadata = items
    .map(
      (item) =>
        `  readonly ${quoted(item.id)}: Readonly<{ idempotency: ${item.contract.idempotency}; idempotencyRequired: ${item.contract.idempotencyRequired}; method: ${quoted(item.method)}; ownership: ${quoted(item.ownership)}; path: ${quoted(item.path)}; public: ${item.public}; requiredCiActions: readonly [${item.metadata.requiredCiActions.map(quoted).join(", ")}]; requiredUserScopes: readonly [${item.metadata.requiredUserScopes.map(quoted).join(", ")}]; risk: ${quoted(item.metadata.risk)}; successStatus: ${item.contract.successStatus} }>;`,
    )
    .join("\n");
  const aliases = items
    .map((item) => `export type ${item.contract.inputName} = ${inputType(item)};`)
    .join("\n");
  const inputMap = items
    .map((item) => `  readonly ${quoted(item.id)}: ${item.contract.inputName};`)
    .join("\n");
  const outputMap = items
    .map((item) => `  readonly ${quoted(item.id)}: Schemas[${quoted(item.contract.resultName)}];`)
    .join("\n");
  const bindings = items
    .map(
      (item) =>
        `  readonly ${quoted(item.id)}: (options${item.contract.inputRequired ? "" : "?"}: ${item.contract.inputName}) => Promise<OperationOutputs[${quoted(item.id)}]>;`,
    )
    .join("\n");
  const required = items
    .filter((item) => item.contract.inputRequired)
    .map((item) => quoted(item.id));
  const optional = items
    .filter((item) => !item.contract.inputRequired)
    .map((item) => quoted(item.id));
  return `// Generated by scripts/openapi/generate.mjs. Do not edit.

import type { JsonValue } from "./index.mjs";

export interface Schemas {
${schemaMap(document)}
}
${aliases}
export declare const operations: Readonly<{
${metadata}
}>;
export type OperationId = keyof typeof operations;
export type RequiredOperationId = ${required.join(" | ")};
export type OptionalOperationId = ${optional.join(" | ")};
export interface OperationInputs {
${inputMap}
}
export interface OperationOutputs {
${outputMap}
}
export interface OperationBindings {
${bindings}
}
export declare function bindOperations(
  request: <Id extends OperationId>(operationId: Id, options: OperationInputs[Id]) => Promise<OperationOutputs[Id]>,
): OperationBindings;
`;
}
